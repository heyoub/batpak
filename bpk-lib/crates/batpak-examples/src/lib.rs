//! Family-wide runnable demos for the batpak family.
//!
//! This crate keeps a tiny library surface for documentation and shared helpers.
//! Runnable programs live as Cargo binary targets under `src/bin/`. Run one with:
//! `cargo run -p batpak-examples --bin quickstart`.
//!
//! It is the single home for demos: there are no per-crate `examples/` folders.
